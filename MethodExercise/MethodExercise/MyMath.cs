using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodExercise
{
    public class MyMath
    {
        public int AddTwoMinusOne(int num1)
        {
            return num1 + num1 - num1;
        }

        public int TimesFourDivideTwice(int num2)
        {
            return num2 * 4 / num2 / num2;
        }

        public int Square(int num3)
        {
            return num3 * num3;
        }
    }
}
